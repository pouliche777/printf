#include "libftprintf.h"

void	ft_putunsigned(unsigned int n)
{
	while (n >= 10)
	{
		ft_putunsigned(n / 10);
		n = n % 10;
	}
	if (n < 10)
	{
		ft_putchar(n + 48);
	}
}
